n1=int(input("Enter first num"))
n2=int(input("Enter second num"))
def sum(n1,n2):
    sum=n1+n2
    return sum
def subtract(n1,n2):
    sub=n1-n2
    return sub
def multiplication(n1,n2):
    mul=n1*n2
    return mul

def division(n1,n2):
    div=n1/n2
    return div

print(sum(n1,n2))
print(subtract(n1,n2))
print(multiplication(n1,n2))
print(division(n1,n2))
